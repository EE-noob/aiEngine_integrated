module ov5640_dri (
    input           clk             ,  //สฑ๏ฟฝ๏ฟฝ
    input           rst_n           ,  //๏ฟฝ๏ฟฝฮป๏ฟฝลบ๏ฟฝ,๏ฟฝอต๏ฟฝฦฝ๏ฟฝ๏ฟฝะง
    //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอท๏ฟฝำฟ๏ฟฝ 
    output          cam_pwdn        ,  //cmos ๏ฟฝ๏ฟฝิด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃสฝัก๏ฟฝ๏ฟฝ๏ฟฝลบ๏ฟฝ
    output          cam_scl         ,  //cmos SCCB_SCL๏ฟฝ๏ฟฝ
    inout           cam_sda         ,  //cmos SCCB_SDA๏ฟฝ๏ฟฝ
    
    //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอท๏ฟฝึฑ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝรฝำฟ๏ฟฝ
    output          cam_init_done     //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝอท๏ฟฝ๏ฟฝสผ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ 
);

//parameter define
parameter SLAVE_ADDR = 7'h3c          ; //OV5640๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝึท7'h3c
parameter BIT_CTRL   = 1'b1           ; //OV5640๏ฟฝ๏ฟฝ๏ฟฝึฝฺต๏ฟฝึทฮช16ฮป  0:8ฮป 1:16ฮป
parameter CLK_FREQ   = 27'd50_000_000 ; //i2c_driฤฃ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝฦต๏ฟฝ๏ฟฝ 
parameter I2C_FREQ   = 18'd250_000    ; //I2C๏ฟฝ๏ฟฝSCLสฑ๏ฟฝ๏ฟฝฦต๏ฟฝ๏ฟฝ,๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ400KHz

//wire difine
wire        i2c_exec       ;  //I2C๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝึด๏ฟฝ๏ฟฝ๏ฟฝลบ๏ฟฝ
wire [23:0] i2c_data       ;  //I2Cาช๏ฟฝ๏ฟฝ๏ฟฝรตฤต๏ฟฝึท๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ(๏ฟฝ๏ฟฝ8ฮป๏ฟฝ๏ฟฝึท,๏ฟฝ๏ฟฝ8ฮป๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ)          
wire        i2c_done       ;  //I2C๏ฟฝฤด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝลบ๏ฟฝ
wire        i2c_dri_clk    ;  //I2C๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ
wire [ 7:0] i2c_data_r     ;  //I2C๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
wire        i2c_rh_wl      ;  //I2C๏ฟฝ๏ฟฝะด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝลบ๏ฟฝ

//*****************************************************
//**                    main code                      
//*****************************************************

//๏ฟฝ๏ฟฝิด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃสฝัก๏ฟฝ๏ฟฝ 0๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃสฝ 1๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝิด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃสฝ
assign  cam_pwdn  = 1'b0;
    
//I2C๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃ๏ฟฝ๏ฟฝ
i2c_ov5640_rgb565_cfg u_i2c_cfg(
    .clk                (i2c_dri_clk),
    .rst_n              (rst_n),
            
    .i2c_exec           (i2c_exec),
    .i2c_data           (i2c_data),
    .i2c_rh_wl          (i2c_rh_wl),        //I2C๏ฟฝ๏ฟฝะด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝลบ๏ฟฝ
    .i2c_done           (i2c_done),     
    .init_done          (cam_init_done) 
    );    

//I2C๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝฤฃ๏ฟฝ๏ฟฝ
i2c_dri #(
    .SLAVE_ADDR         (SLAVE_ADDR),       //๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ
    .CLK_FREQ           (CLK_FREQ  ),              
    .I2C_FREQ           (I2C_FREQ  ) 
    )
u_i2c_dri(
    .clk                (clk),
    .rst_n              (rst_n     ),

    .i2c_exec           (i2c_exec  ),   
    .bit_ctrl           (BIT_CTRL  ),   
    .i2c_rh_wl          (i2c_rh_wl),        //๏ฟฝฬถ๏ฟฝฮช0๏ฟฝ๏ฟฝึป๏ฟฝรต๏ฟฝ๏ฟฝ๏ฟฝIIC๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝะด๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝ   
    .i2c_addr           (i2c_data[23:8]),   
    .i2c_data_w         (i2c_data[7:0]),   
    .i2c_data_r         (i2c_data_r),   
    .i2c_done           (i2c_done  ),
    
    .scl                (cam_scl   ),   
    .sda                (cam_sda   ),   

    .dri_clk            (i2c_dri_clk)       //I2C๏ฟฝ๏ฟฝ๏ฟฝ๏ฟฝสฑ๏ฟฝ๏ฟฝ
    );

endmodule 